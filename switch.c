#include <stdio.h>

int main ()
{
  int counter = 16 ;
  switch (counter)
    {
    case 1: 
      printf("one\n");
      break;
    case 2:
      printf("two\n");
      break;
    case 3:
      printf("Three\n");
	break;
    case 4:
      printf("four\n");
      break;
    default:
      printf("None of the above\n");
    }
}
